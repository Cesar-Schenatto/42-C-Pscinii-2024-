#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	*str;

	i = ac - 1;
	while (i > 0)
	{
		str = av[i];
		while (*str)
		{
			write(1, str, 1);
			str++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
